# Comparison Operators
# they return a boolean value (True or False) 
d=5<=5 # greater than or equal to 
print(d)
print(1==1)

# Logical Operators
# Note :- True and False are case-sensitive in Python
e=True or False # logical OR
print(e)

# Truth Table for OR and AND
